package com.tka.HW;

public class Method_Ex1 {
          int x=10,y=20;
          float z;
          
          void add()
          {
        	  z=x+y;
        	System.out.println("display add (z)="+z);  
          }
          void sub()
          {
        	  z=x-y;
        	System.out.println("display sub (z)="+z);  
          }
          void multi()
          {
        	  z=x*y;
        	System.out.println("display multi (z)="+z);  
          }
          void div()
          {
        	  z=x/y;
        	System.out.println("display div (z)="+z);  
          }
     
}
