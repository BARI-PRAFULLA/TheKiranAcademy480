package com.tka.encapsulation;

public class Student {
           private int studid;
           private String studnm;
           private double studfees;
		public int getStudid() {
			return studid;
		}
		public void setStudid(int studid) {
			this.studid = studid;
		}
		public String getStudnm() {
			return studnm;
		}
		public void setStudnm(String studnm) {
			this.studnm = studnm;
		}
		public double getStudfees() {
			return studfees;
		}
		public void setStudfees(double studfees) {
			this.studfees = studfees;
		}
}
